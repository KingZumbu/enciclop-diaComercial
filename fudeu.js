let produto1 = null
let produto2 = null

function sorteiaproduto(){
    const min = 1;
    const max = 15;

    const primeiroNumero = Math.floor(Math.random() * (max - min + 1)) + min;

    let segundoNumero;
    do {
    segundoNumero = Math.floor(Math.random() * (max - min + 1)) + min;
    } while (segundoNumero === primeiroNumero);

    produto1 = produto[primeiroNumero.toString()]
    produto2 = produto[segundoNumero.toString()]
}

function escolherproduto(){
    document.getElementById("produto1nome").textContent = produto1.nome
    document.getElementById("produto2nome").textContent = produto2.nome

    document.getElementById("produto1preco").textContent = produto1.preco
    document.getElementById("produto2preco").textContent = produto2.preco

    document.getElementById("produto1img").src = produto1.img
    document.getElementById("produto2img").src = produto2.img

    document.getElementById("produto1comprar").href = produto1.link
    document.getElementById("produto2comprar").href = produto2.link

}


document.addEventListener("DOMContentLoaded", function() {
    sorteiaproduto();
    escolherproduto();
});
